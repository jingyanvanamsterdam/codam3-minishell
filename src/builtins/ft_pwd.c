#include "minishell.h"
#include "libft.h"
#include <unistd.h>

// return 0 if the function successes, return 1 otherwise
int	ft_pwd(char **argv, t_shell *shell)
{
	char	*cwd;

	(void)argv;
	(void)shell;
	cwd = NULL;
	cwd = getcwd(cwd, 0);
	if (!cwd)
		return (1);
	ft_putstr_fd(cwd, 1);
	ft_putstr_fd("\n", 1);
	free(cwd);
	return (0);
}
